#include <stdio.h>

int main(void) {
	int a = 4;
	printf("Starting value of a: %i\n", a);
	printf("Value after a++: %i\n", a++);
	printf("Value after a--: %i\n", a--);
	printf("Value after ++a: %i\n", ++a);
	printf("Value after --a: %i\n", --a);
	printf("Value after -a: %i\n", -a);
	printf("Value after +a: %i\n", +a);
}
